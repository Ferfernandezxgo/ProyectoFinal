    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        try{
        
            Integer dni = Integer.parseInt(jtDni.getText());
            pacienteActual= pdata.buscarPacientePorDni(dni);
            if (pacienteActual!=null){
            
                jtNombre.setText(pacienteActual.getNombre());
                jtDomicilio.setText(pacienteActual.getDomicilio());
                jtTelefono.setText(pacienteActual.getTelefono());
                jrEstado.setSelected(pacienteActual.isEstado());
            }
        
        }catch(NumberFormatException ex){
        
           JOptionPane.showMessageDialog(this, "Debe ingresar un numero valido");
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jbNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNuevoActionPerformed
        // TODO add your handling code here:
        limpiarcampos();
        pacienteActual=null;
    }//GEN-LAST:event_jbNuevoActionPerformed

    private void JbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbEliminarActionPerformed
        // TODO add your handling code here:
        if (pacienteActual!=null){
        
            pdata.eliminarPaciente(pacienteActual.getIdPaciente());
            pacienteActual=null;
            limpiarcampos();
            
        } else{
        
            JOptionPane.showMessageDialog(this, "No hay un alumno seleccionado");
        }
    }//GEN-LAST:event_JbEliminarActionPerformed

    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        try{
            Integer dni = Integer.parseInt(jtDni.getText());
            String nombre = jtNombre.getText();
            if (nombre.isEmpty()){
               JOptionPane.showMessageDialog(this, "No puede haber campos vacios");
               return;
            }
            String domicilio =jtDomicilio.getText();
            String telefono = jtTelefono.getText();
            Boolean estado =jrEstado.isSelected();
            //Integer idpaciente = Integer.parseInt(jTIdPaciente.getText());
            
            if (pacienteActual==null){
             
                pacienteActual=new Paciente(dni,nombre,domicilio,telefono,estado);
                pdata.insertarPaciente(pacienteActual);
            }else{
            
               pacienteActual.setDni(dni);
               pacienteActual.setNombre(nombre);
               pacienteActual.setDomicilio(domicilio);
               pacienteActual.setTelefono(telefono);
               pdata.ActualizarPaciente(pacienteActual);
            }
        
        }catch(NumberFormatException nfe){
        
           JOptionPane.showMessageDialog(this, "No se pudo guardar el paciente");
        }
        
    }//GEN-LAST:event_jbGuardarActionPerformed

    public void limpiarcampos(){
    
        jtDni.setText("");
        jtNombre.setText("");
        jtDomicilio.setText("");
        jtTelefono.setText("");
        jrEstado.setSelected(true);
    }

